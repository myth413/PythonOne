# num caluate
print(2 + 3)  # 2+3=5
print(3 - 2)  # 3-2=1
print(2 * 3)  # 2*3=6
print(3 / 2)  # 3/2=1.5

print(3 ** 2)  # 3 ** 2 = 9
print(3 ** 3)  # 3 ** 3 = 27
print(10 ** 6)  # 10 ** 6 = 1000000

print(0.1 + 0.1)  # 0.2
print(0.2 + 0.1)  # 0.30000000000000004
print(2 * 0.1)  # 0.2
print(3 * 0.1)  # 0.30000000000000004