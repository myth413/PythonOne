#数值型
n = 100
#type, 查看变量的数据类型
print(type(n))
pi = 3.14
print(type(pi))
'''
<class 'int'>
<class 'float'>
'''

#字符型
string1 = "Python"
print(type(string1))
string2 = 'Python'
print(type(string2))
'''
<class 'str'>
<class 'str'>
'''

#多行字符串
string3 = '''Python
baidu
'''
print(string3)
print(type(string3))
'''
Python
baidu

<class 'str'>
'''
